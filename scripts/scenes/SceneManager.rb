module SceneManager
	@scene = nil                            # current scene object
	@stack = []
	#--------------------------------------------------------------------------
	# * Start
	#--------------------------------------------------------------------------
	def self.run(*args)
		InitWindow(args[0],args[1],args[2])
		InitAudioDevice()
		SetTargetFPS(args[3])
		#ToggleFullscreen()
		#SetWindowMonitor(1)
		@scene = first_scene_class.new
		@scene.main while @scene
	end
	#--------------------------------------------------------------------------
	# * Scene
	#--------------------------------------------------------------------------
	def self.scene
		@scene
	end
	#--------------------------------------------------------------------------
	# * First Scene
	#--------------------------------------------------------------------------
	def self.first_scene_class
		Scene_Title
  end
	#--------------------------------------------------------------------------
	# * Determine Current Scene Class
	#--------------------------------------------------------------------------
	def self.scene_is?(scene_class)
		@scene.instance_of?(scene_class)
	end
	#--------------------------------------------------------------------------
	# * Direct Transition
	#--------------------------------------------------------------------------
	def self.goto(scene_class)
		@scene = scene_class.new
	end
	#--------------------------------------------------------------------------
	# * Call
	#--------------------------------------------------------------------------
	def self.call(scene_class)
		@stack.push(@scene)
		@scene = scene_class.new
	end
	#--------------------------------------------------------------------------
	# * Return to Caller
	#--------------------------------------------------------------------------
	def self.return
		@scene = @stack.pop
	end
	#--------------------------------------------------------------------------
	# * Clear Call Stack
	#--------------------------------------------------------------------------
	def self.clear
		@stack.clear
	end
	#--------------------------------------------------------------------------
	# * Exit Game
	#--------------------------------------------------------------------------
	def self.exit
		@scene = nil
		CloseAudioDevice()
		CloseWindow()
	end
end
