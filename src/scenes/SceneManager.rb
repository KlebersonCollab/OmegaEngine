module SceneManager
  @scene = nil                            # current scene object
  @stack = []
  #--------------------------------------------------------------------------
  # * Start
  #--------------------------------------------------------------------------
  def self.run
    @scene = first_scene_class.new
  end
  #--------------------------------------------------------------------------
  # * Scene
  #--------------------------------------------------------------------------
  def self.scene
    @scene
  end
  #--------------------------------------------------------------------------
  # * Scene
  #--------------------------------------------------------------------------
  def self.update
    @scene.update unless @scene.nil?
  end
  #--------------------------------------------------------------------------
  # * First Scene
  #--------------------------------------------------------------------------
  def self.first_scene_class
    Scene_Title
  end
  #--------------------------------------------------------------------------
  # * Determine Current Scene Class
  #--------------------------------------------------------------------------
  def self.scene_is?(scene_class)
    @scene.instance_of?(scene_class)
  end
  #--------------------------------------------------------------------------
  # * Direct Transition
  #--------------------------------------------------------------------------
  def self.goto(scene_class)
    @scene = scene_class.new
  end
  #--------------------------------------------------------------------------
  # * Call
  #--------------------------------------------------------------------------
  def self.call(scene_class)
    @stack.push(@scene)
    @scene = scene_class.new
  end
  #--------------------------------------------------------------------------
  # * Return to Caller
  #--------------------------------------------------------------------------
  def self.return
    @scene = @stack.pop
  end
  #--------------------------------------------------------------------------
  # * Clear Call Stack
  #--------------------------------------------------------------------------
  def self.clear
    @stack.clear
  end
  #--------------------------------------------------------------------------
  # * Exit Game
  #--------------------------------------------------------------------------
  def self.exit
    @scene.dispose
    @scene = nil
  end
end
